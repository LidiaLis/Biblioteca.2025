    private void atualizarTabela(String textoDigitado) {
    DefaultTableModel model = (DefaultTableModel) tblAcervo.getModel();
    model.setRowCount(0); // limpa a tabela

    UsuarioDAO dao = new UsuarioDAOJDBC();
    List<Usuario> usuarios = dao.listarPorNome(textoDigitado);

    if (usuarios.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Nenhum usuário encontrado com esse nome.");
    }

    for (Usuario usuario : usuarios) {
        model.addRow(new Object[]{
            usuario.getId_usuario(),
            usuario.getNome(),
            usuario.getTelefone(),
            usuario.getEmail()
        });
    }
}
